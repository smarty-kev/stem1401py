"""
Operators in python

1. Arithmetic operators

"""

# arithmetic
x = 5
y = 12
result = x + y
print("x + y = {}\n".format(result))

result = x - y
print("x - y = {}\n".format(result))

result = x * y
print("x * y = {}\n".format(result))

result = x / y
print("x / y = {}\n".format(result))

# modulus
result = x % y
print("x % y = {}\n".format(result))

# floor division
result = 5 // 2
print("5 // 2 = {}\n".format(result))

x = 4
y = 3
result = x ** y
print("x ** y = {}\n".format(result))