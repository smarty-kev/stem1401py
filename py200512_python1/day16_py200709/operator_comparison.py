x = 5
y = 12
print("x={}, y={}".format(x,y))

# >
print("x > y = {}".format(x > y))

# <
print("x < y = {}".format(x < y))

# >=
print("5 >= 5 = {}".format(5 >= 5))

# <=
print("x <= y = {}".format(x <= y))

# ==
print("x == y = {}".format(x == y))

# !=
print("x != y = {}".format(x != y))

